void GLCD_CreateImage(const char *image){
    unsigned int i=0,j=0;
    int page[]={0xB8,0xB9,0xBA,0xBB,0xBC,0xBD,0xBE,0xBF};


    HAL_Delay(1);
    GLCD_WriteCommand(0xC0);    //Start line, (64 rows) selects from where to start(1 line)
    for(i=0;i<=7;i++){
        GLCD_PageSelect(0);
        GLCD_WriteCommand(page[i]);
        GLCD_WriteCommand(0x40);    //Setting y-address
        for(j=0+(i*128);j<=63+(i*128);j++){
            GLCD_WriteData(image[j]);
        }
        GLCD_PageSelect(1);
        GLCD_WriteCommand(page[i]);
        GLCD_WriteCommand(0x40);    //Setting y-address
        for(j=64+(i*128);j<=127+(i*128);j++){
        	GLCD_WriteData(image[j]);
        }
        j=0;
    }
}

